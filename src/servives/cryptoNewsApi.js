    'x-bingapis-sdk': 'true',
    'x-rapidapi-host': 'bing-news-search1.p.rapidapi.com',
    'x-rapidapi-key': 'e66afa9f43msh15308a86c28d4fep1e13b4jsn46d5f9c82d88'
